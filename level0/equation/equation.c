#include <stdio.h>

void    equation(int n)
{
	size_t	A = 0;
	size_t	B;
	size_t	C;

	while ((int)A < 10)
	{
		B = 0;
		C = 9;
		while (B < 10)
		{
			C = 9;
			while (C < 10)
			{
				if ((int)(((A * 10) + B) + ((C * 10) + A)) == n)
					printf("A = %zu, B = %zu, C = %zu\n", A, B, C);
				C--;
			}
			B++;
		}
		A++;
	}
}
