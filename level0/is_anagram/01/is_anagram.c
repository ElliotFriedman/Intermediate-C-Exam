int	is_anagram(char *a, char *b)
{
	int		cmp[128];
	int		i;

	i = 0;
	while (i < 128)
	{
		cmp[i] = 0;
		i++;
	}
	i = 0;
	while (a[i])
	{
		cmp[(int)a[i]] += 1;
		i++;
	}
	i = 0;
	while (b[i])
	{
		cmp[(int)b[i]] -= 1;
		i++;
	}
	i = 0;
	while (i < 128)
	{
		if (cmp[i])
			return 0;
		i++;
	}
	return 1;
}
